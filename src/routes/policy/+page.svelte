	<h2 class="subtitle medium">Data Collection</h2>

	<section>
		We collect the following personal data:

		<ol>
			<li>Student Names</li>
			<li>Student IDs</li>
		</ol>

		This data is collected exclusively for the purpose of BitHunt and will not be used for any other
		applications or transferred to third parties.
	</section>

	<h2 class="subtitle medium">Usage of Data</h2>

	<section>
		The data we collect is used for the following:

		<ol>
			<li>Identifying participants playing BitHunt.</li>
			<li>
				Populating Leaderboards, but only for users who have given explicit consent to have their
				name and student ID displayed.
			</li>
		</ol>
	</section>

	<h2 class="subtitle medium">Data Storage and Protection</h2>

	<section>
		We employ reasonable data protection measures to safeguard the data collected. Your data is
		stored in a secure environment and is accessible only by authorized personnel of the CS++
		College Society.
	</section>

	<h2 class="subtitle medium">User Rights</h2>

	<section>
		As a user of the BitHunt app, you have the right to:

		<ol>
			<li><strong>Access: </strong> Request a copy of the data we have about you.</li>
			<li><strong>Rectification: </strong> Update or modify your data if it's incorrect.</li>
			<li><strong>Erasure: </strong> Request removal of your data from our database.</li>
		</ol>
	</section>

	<h2 class="subtitle medium">Data Removal</h2>

	<section>
		Your data will be automatically removed at the end of every BitHunt event. If you wish to have
		your data removed before the end of the event, you can send a request to our team.
	</section>

	<h2 class="subtitle medium">Leaderboard Visibility</h2>

	<section>
		Users have the option to allow their names and student IDs to be visible on the BitHunt
		leaderboard. This feature is opt-in, and unless explicit consent is given, personal user data
		will not be displayed publicly, instead being replaced with a random pseudonym.
	</section>

	<h2 class="subtitle medium">Changes to Privacy Policy</h2>

